package com.lab6.io;

public interface Source<T> {
    void load(T t);     // - восстановление состояния объекта из некоторого источника
    void store(T t);    // – запись состояния объекта в некоторый источник
    void delete(T t);   // – удаляющий информацию о состоянии объекта из некоторого источника
    void create(T t);   // - создающий новый источник, хранящий состояние объекта
}
